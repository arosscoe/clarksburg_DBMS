    private void goButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_goButtonActionPerformed
        if (!StudentRadio.isSelected() && !StaffRadio.isSelected() && !GuardianRadio.isSelected() && !GradeReportRadio.isSelected() && !GradeLevelRadio.isSelected()) {
            //do nothing
        } else if (StudentRadio.isSelected() && !StaffRadio.isSelected() && !GuardianRadio.isSelected() && !GradeReportRadio.isSelected() && !GradeLevelRadio.isSelected()) {
            dispose();
            new SearchStudent().setVisible(true);
        } else if (!StudentRadio.isSelected() && StaffRadio.isSelected() && !GuardianRadio.isSelected() && !GradeReportRadio.isSelected() && !GradeLevelRadio.isSelected()) {
            dispose();
            new SearchStaff().setVisible(true);
        } else if (!StudentRadio.isSelected() && !StaffRadio.isSelected() && GuardianRadio.isSelected() && !GradeReportRadio.isSelected() && !GradeLevelRadio.isSelected()) {
            dispose();
            new SearchGuardian().setVisible(true);
        } else if (!StudentRadio.isSelected() && !StaffRadio.isSelected() && !GuardianRadio.isSelected() && GradeReportRadio.isSelected() && !GradeLevelRadio.isSelected()) {
            dispose();
            new SearchGradeReport().setVisible(true);
        } else if (!StudentRadio.isSelected() && !StaffRadio.isSelected() && !GuardianRadio.isSelected() && !GradeReportRadio.isSelected() && GradeLevelRadio.isSelected()) {
            dispose();
            new SearchGradeLevel().setVisible(true);
        }
    }//GEN-LAST:event_goButtonActionPerformed

